// String
let empName = 'John';
console.log(`value : ${empName} Type ${typeof empName}`);

// Number
let empAge = 40;
console.log(`value : ${empAge} Type ${typeof empAge}`);

// Boolean
let isActive = true;
console.log(`value : ${isActive} Type ${typeof isActive}`);

// undefined
let abc;
console.log(`value : ${abc} Type ${typeof abc}`);

// null
let isPromoted = null;
console.log(`value : ${isPromoted} Type ${typeof isPromoted}`);

// Reassignment
let test;
console.log(`value : ${test} Type ${typeof test}`);

test = 100;
console.log(`value : ${test} Type ${typeof test}`);

test = 'john';
console.log(`value : ${test} Type ${typeof test}`);

test = true;
console.log(`value : ${test} Type ${typeof test}`);

test = null;
console.log(`value : ${test} Type ${typeof test}`);